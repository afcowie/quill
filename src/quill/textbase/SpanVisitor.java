package quill.textbase;

/**
 * Visit the Spans in a range, in order.
 * 
 * @author Andrew Cowie
 */
public interface SpanVisitor
{
    /**
     * @param span
     *            The next Span being visted in the tree.
     * @return return true to indicate you're done and to abort the tree walk,
     *         otherwise return false to cause the walk to continue.
     */
    public boolean visit(Span span);
}
